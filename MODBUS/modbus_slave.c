// 计算CRC16
static uint16_t Get_CRC16(unsigned char *pucFrame, uint16_t usLen) // 计算CRC的公式
{
	unsigned char ucCRCHi = 0xFF;
	unsigned char ucCRCLo = 0xFF;
	u16 iIndex = 0;

	while (usLen--)
	{
		iIndex = ucCRCLo ^ *(pucFrame++);
		ucCRCLo = (unsigned char)(ucCRCHi ^ aucCRCHi[iIndex]);
		ucCRCHi = aucCRCLo[iIndex];
	}
	Data8To16(usLen, ucCRCHi, ucCRCLo);
	return usLen;
}

static void Attach_CRC16(unsigned char *pucFrame, uint16_t dataLen) // 用来给某一帧的帧尾加入CRC
{
	unsigned char ucCRCHi = 0xFF;
	unsigned char ucCRCLo = 0xFF;
	uint16_t usLen = dataLen;
	u16 iIndex = 0;
	unsigned char *p = pucFrame;

	while (usLen--)
	{
		iIndex = ucCRCLo ^ *(pucFrame++);
		ucCRCLo = (unsigned char)(ucCRCHi ^ aucCRCHi[iIndex]);
		ucCRCHi = aucCRCLo[iIndex];
	}
#if CRC_ORDER
	p[dataLen + 1] = ucCRCHi;
	p[dataLen] = ucCRCLo;
#else
	p[dataLen] = ucCRCHi;
	p[dataLen + 1] = ucCRCLo;
#endif
}
// count是整个帧在加入CRC之后的字节数，前面count-2个字节是数据，后面2个字节是CRC
static unsigned char Check_CRC16(unsigned char *p, u16 count) // 这里比的是收到的CRC和计算出的CRC
{
	u16 crc = 0;
#if CRC_ORDER
	Data8To16(crc, p[count - 1], p[count - 2]);
#else
	Data8To16(crc, p[count - 2], p[count - 1]); // 把后面两个字节由8位合并为16位，这里是数据发送过来自带的CRC值
#endif
	if (crc == Get_CRC16(p, count - 2)) // 前面count-2个字节是数据，后面两个字节是CRC值
		return 1;
	return 0;
}

void ModBusRecByte(u8 data)
{
	if (MState == MODBUS_IDLE) // 如果从机此时是空闲状态
	{
		RecBuff[0] = data; // 接受第一个字节的数据
		RecCount = 1;
		FUN_RESET_TIMER();		// 把从机定时器TIM6的计数值重置
		FUN_START_TIMER();		// 从机定时器TIM6开始计时
		MState = MODBUS_RECING; // 该状态改成RECING
	}
	else if (MState == MODBUS_RECING)
	{
		RecBuff[RecCount++] = data; // RECING状态下就一直接受数据，最大能够一次性接受1024个字节
		if (RecCount >= MODBUS_BUFF_LEN)
			RecCount = 0;
		FUN_RESET_TIMER();
	}
}

